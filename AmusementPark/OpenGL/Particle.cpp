Particle::~Particle()
{
	if (m_vao)
		delete m_vao;
}

bool Particle::Update(float t)
{
	position += velocity * t;
	position.y -= gravityEffect * t;


	m_elapsedTime += t;
	return (m_elapsedTime > lifeTime);
}

void Particle::Draw(Shader* shader)
{
	shader->Use();

	glm::mat4 model = glm::mat4(1.0f);
	model = glm::translate(model, position);
	model = glm::scale(model, scale);

	shader->setMat4("u_model", model);
	shader->setFloat3("u_color", color);


	m_vao->Bind();
	glDrawArrays(GL_TRIANGLES, 0, 36);
}
